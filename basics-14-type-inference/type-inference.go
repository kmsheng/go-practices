package main

import "fmt"

func main() {
    num := 1
    floatNum := 0.2
    str := "str"
    imaginaryNum := 0.5i
    fmt.Printf("num is of type %T\n", num)
    fmt.Printf("floatNum is of type %T\n", floatNum)
    fmt.Printf("str is of type %T\n", str)
    fmt.Printf("imaginaryNum is of type %T\n", imaginaryNum)
}
